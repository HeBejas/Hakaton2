		<h2>Возрождение Мувыра</h2>
		<div class="about_content_block">
			Сначала Александр заготавливал лес, разводил овец, обрабатывал землю.
			Даже страусов завёл — для привлечения внимания к деревне. 
			Теперь руководителя фермерского хозяйства Корепанова в Удмуртии знают многие. 
			На деньги жителей проложены 4 км гравийной дороги, закуплена техника, возведены две водонапорные башни.
			А материалы для постройки домов Александр Геннадьевич предоставляет переселенцам безвозмездно. 
			Зарплаты в хозяйстве — 25–30 тыс. руб. Вроде немного. Но работники и зерно получают, и дрова для отопления.
		
			Благодаря поддержке региональных властей, в деревне был открыт собственный цех по переработке молока. 
			Под торговой маркой «Мувыр» здесь производят кефир, йогурт, творог и сыр.
			Одна из дочерей Александра Корепанова, Нина Шкляева, работает управляющей на этом молокозаводе.
			Окончив школу, Нина поступила в педагогический институт и переехала в Пермь. 
			Поездки в родную деревню дали понять, что жить в городе ей не нравится, 
			и спустя несколько лет Нина решила вернуться в Мувыр.
			Когда регион выделил грант на строительство молокозавода, 
			жизнь Нины изменилась, и она начала развиваться в области фермерского хозяйства.
