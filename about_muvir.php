			Вторая дочь Александра Корепанова, Мария Прокофьева, отвечает за туризм в деревне.
			Жизнь в Москве показалась ей суетливой, а офисная работа — 
			однообразной, поэтому во время декрета Мария решила вернуться в Мувыр.
			Постепенно дочь Корепанова начала заниматься туризмом:
			она проводит экскурсии, открыла музей и кафе, приобрела лодки, занялась постройкой гостевых домов.
