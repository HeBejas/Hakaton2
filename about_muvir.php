		<h2>Мувыр Сейчас</h2>
		<div class="about_content_block">
			Александр Геннадьевич за всё в ответе. 
			За людей (в Мувыре работают и жители других деревень), 
			за крупный рогатый скот. Общее поголовье — 350 особей, 
			из них 150 — дойные коровы. И на всю продукцию есть покупатель, 
			даже оптовый — натуральное фермерское народ уважает.
			Пора кафе открывать да кормить туристов. Хотя с ними непросто. 
			Путешественников и в удмуртской столице немного. 
			А от Ижевска до Мувыра 120 км. Не каждый готов сделать крюк ради пейзажей и модного агротуризма.
			Привлечением гостей занимается младшая дочь Корепанова. Когда началась пандемия,
			Мария с семьёй бросила работу в Москве и переехала в Мувыр.
			И потянулись школьники и взрослые в деревню, в которой есть крутая детская площадка, 
			верёвочный экстрим-парк, каскад зарыбленных прудов, где можно посидеть не с гаджетом, а с удочкой...
