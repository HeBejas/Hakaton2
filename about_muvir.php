		<h2>История мувыра</h2>
		<div class="about_content_block">
			В начале 1980-х родную деревню Корепанова, основанную в 1837-м, стёрли с лица земли. 
			Дома снесли бульдозером, людей перебросили в соседние сёла. 
			«Неперспективный» Мувыр перестал существовать в документах и на картах.
			А Александр никак не мог понять: зачем и кому это нужно?
			Он сидел на берегу реки, смотрел на поле, где ещё недавно кипела жизнь, и у него, 
			молодого сильного мужчины, отслужившего срочную на границе с Китаем, опускались руки.
			Именно тогда он пообещал себе возродить малую родину. Но что мог 20-летний дембель? 
			Сегодня «молодому сильному мужчине» 62.
			
			И уже полжизни он восстанавливает деревню Мувыр в Игринском районе Удмуртии, 
			на берегу реки Лоза. Сразу после армии такую ношу было не потянуть.
			Поэтому десять лет Корепанов прожил в соседней Зуре.
			Помогал родителям по хозяйству, учился заочно в сельхозинституте.
			И всё время твердил: «Надо возвращаться в Мувыр...» Отец однажды не выдержал:
			«Ну, и сколько ты ещё болтать будешь?»— В детстве я смотрел на него — тракториста,
			механизатора, передовика — и понимал, что тоже хочу связать жизнь с землёй
			, — вспоминает Александр. — Отец воспитывал меня не словами, а личным примером. 
			Мне хотелось ему помогать. Я гордился нашей деревней. 
			И даже представить себе не мог, что когда-нибудь она исчезнет и мне придётся её возрождать.
			
